#ifndef _ZEBRA_VARGS_H
#define _ZEBRA_VARGS_H

#include <stdarg.h>

/* GCC has printf type attribute check.         */
#ifdef __GNUC__
#define PRINTF_ATTRIBUTE(a,b) __attribute__ ((__format__ (__printf__, a, b)))
#else
#define PRINTF_ATTRIBUTE(a,b)
#endif /* __GNUC__ */

#endif /* _ZEBRA_VARGS_H */
